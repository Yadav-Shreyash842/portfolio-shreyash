'use client'

import React, { useState } from 'react'
import Image from 'next/image'
import { assets } from '@/assets/assets'

const Contact = () => {
  const [result, setResult] = useState('')

  const onSubmit = async (event) => {
    event.preventDefault()
    setResult('Sending...')

    const formData = new FormData(event.target)
    formData.append('access_key', 'e50ba21d-7177-442a-ab55-8b82c51db08e')

    try {
      const response = await fetch('https://api.web3forms.com/submit', {
        method: 'POST',
        body: formData,
      })

      const data = await response.json()

      if (data.success) {
        setResult('✅ Thank you! Your message has been sent.')
        event.target.reset()
      } else {
        setResult('❌ Something went wrong. Please try again.')
      }
    } catch (error) {
      setResult('❌ Network error. Please try again later.')
    }
  }

  return (
    <div
      id="contact"
      className='w-full px-[12%] py-10 scroll-mt-20 
                 bg-[url("/footer-bg-color.png")] bg-no-repeat 
                 bg-center bg-[length:90%_auto]'
    >
      <h4 className="text-center mb-2 text-lg font-ovo">Connect with me</h4>
      <h2 className="text-center text-5xl font-ovo mb-8">Get in touch</h2>

      <p className="text-center max-w-2xl mx-auto mt-5 mb-12 font-ovo">
        I'd love to hear from you! If you have any questions, comments, or feedback,
        please use the form below.
      </p>

      <form onSubmit={onSubmit} className="max-w-2xl mx-auto">
        <div className="grid grid-cols-1 md:grid-cols-2 gap-6 mt-10 mb-8">
          <input
            type="text"
            name="name"
            placeholder="Enter your name"
            required
            autoComplete="off"
            className="p-3 outline-none border border-gray-400 rounded-md bg-white w-full"
          />
          <input
            type="email"
            name="email"
            placeholder="Enter your email"
            required
            autoComplete="off"
            className="p-3 outline-none border border-gray-400 rounded-md bg-white w-full"
          />
        </div>

        <textarea
          name="message"
          rows="6"
          placeholder="Enter your message"
          required
          className="w-full p-4 outline-none border border-gray-400 rounded-md bg-white mb-6"
        ></textarea>

        <button
          type="submit"
          className="py-3 px-8 w-max flex items-center justify-between gap-2 
                     bg-black/80 text-white rounded-full mx-auto 
                     hover:bg-black transition duration-300"
        >
          Submit now
          <Image
            src={assets.right_arrow_white}
            alt="Right arrow"
            width={16}
            height={16}
            className="w-4 h-4"
          />
        </button>

        {result && (
          <p className="text-center text-sm mt-4 text-gray-700">{result}</p>
        )}
      </form>
    </div>
  )
}

export default Contact